from enum import StrEnum

class EWMEnum(StrEnum):
    label: str

    def __new__(cls, value, label):
        obj = str.__new__(cls, value)
        obj._value_ = value
        obj.label = label
        return obj

    def __str__(self) -> str:
        return self.label

    def __repr__(self):
        return f"<{self.__class__.__name__}.{self.name}: '{self.value}' ({self.label})>"
    
    @classmethod
    def _missing_(cls, value):
        try:
            return cls.RESERVED
        except AttributeError:
            raise ValueError(f"{value!r} is not a valid {cls.__name__}")

